    public void FirstController(){
        try {
            UserDAO dao = new UserDAO();
            List<UserDTO> dtoList = dao.getAllUser();
            List<UserDTO> dtoListAdmin = dao.getListAdmin();
            List<UserDTO> dtoListUser = dao.getListUser();
            showAllUser(dtoList);
            showAdmin(dtoListAdmin);
            showUser(dtoListUser);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void showAllUser(List<UserDTO> list) throws Exception{
        tblModelAll.setRowCount(0);
        for (UserDTO userDTO : list) {
            tblModelAll.addRow(userDTO.toVector());
        }  
    }
    public void showAdmin(List<UserDTO> list) throws Exception{
        tblModelAdmin.setRowCount(0);
        for (UserDTO userDTO : list) {
            tblModelAdmin.addRow(userDTO.toVector());
        }  
    }
    public void showUser(List<UserDTO> list) throws Exception{
        tblModelUser.setRowCount(0);
        for (UserDTO userDTO : list) {
            tblModelUser.addRow(userDTO.toVector());
        }  
    }

