    private void table_AllMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_AllMouseClicked
        try {
            int row = table_All.getSelectedRow();
            String userId = (String) tblModelAll.getValueAt(row, 0);
            UserDAO dao = new UserDAO();
            UserDTO dto = dao.getOneUser(userId);
            String role = dao.getRoleByuserID(userId);
            txt_UserID.setText(dto.getIdUser());
            txt_UserName.setText(dto.getUserName());
            txt_Email.setText(dto.getEmail());
            txt_Phone.setText(dto.getPhone());
            txt_PhotoName.setText(dto.getPhotoName());
            if (role.equals("admin")) {
                radio_admin.setSelected(true);
            }else if(role.equals("user")){
                radio_user.setSelected(true);
            }else{
                radio_admin.setSelected(false);
                radio_user.setSelected(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_table_AllMouseClicked

